
<h2>&laquo;Умный дом&raquo; - что это такое и как нам жить дальше...
  <br />
</h2>
 	
<p>Умный дом это система, которая обеспечивает Ваш комфорт (в том числе безопасность), и рациональное использование ресурсов системы жизнедеятельности вашего дома. Время не стоит на месте. Наступил момент, когда вы сможете настроить ваш дом так, как вы этого хотите! Вы сможете даже общаться со своим домом, например через смс.
